    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \App\Models\Member  $member
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, Member $member)
    {
         $request->validate([
            'job' => 'required',
        ]);

        $member->job = $request->job;
        if ($request->image) {
            Storage::disk('public')->delete('img/team/' . $member->image);
            $member->image = $request->file('image')->hashName();
            $request->file('image')->storePublicly('img/team', 'public');
            
        }
        $member->updated_at = now();
        $member->save();

        return redirect()->route('team.index')->with('message', 'Member has been updated');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\Models\Member  $member
     * @return \Illuminate\Http\Response
     */
    public function destroy(Member $member)
    {
        $member->delete();
        return redirect()->back()->with('message', 'Member has been deleted');
    }
